// event listener for submit event from signup form
async function signupFormEventHandler(event) {
  event.preventDefault();

  const username = document.querySelector("#username-signup").value.trim();
  const email = document.querySelector("#email-signup").value.trim();
  const password = document.querySelector("#password-signup").value.trim();

  //   if all fields contain values, make post request
  if (username && email && password) {
    const response = await fetch("/api/users", {
      method: "post",
      body: JSON.stringify({
        username,
        email,
        password,
      }),
      headers: { "Content-Type": "application/json" },
    });

    // check response status
    if (response.ok) {
      console.log("successful");
      //   unsuccessful response triggers error
    } else {
      alert(response.statusText);
    }
  }
}

// event listener for login event from login form
async function loginFormEventHandler(event) {
  event.preventDefault();

  const email = document.querySelector("#email-login").value.trim();
  const password = document.querySelector("#password-login").value.trim();

  if (email && password) {
    const response = await fetch("/api/users/login", {
      method: "post",
      body: JSON.stringify({
        email,
        password,
      }),
      headers: { "Content-Type": "application/json" },
    });

    if (response.ok) {
      // after login, automatically redirect user to dashboard
      document.location.replace("/dashboard");
    } else {
      alert(response.statusText);
    }
  }
}

document
  .querySelector(".signup-form")
  .addEventListener("submit", signupFormEventHandler);

document
  .querySelector(".login-form")
  .addEventListener("submit", loginFormEventHandler);
